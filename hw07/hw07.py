    def two_list_helper(vals, counts, val, count, lnk, store_lnk):
        if count == 0:
            if len(vals) != 0:
                return two_list_helper(vals[1:], counts[1:], vals[0], counts[0], lnk, store_lnk)
            return store_lnk
        else:
            count -= 1
            lnk.rest = Link(val)
            return two_list_helper(vals, counts, val, count, lnk.rest, store_lnk)
    
    val = vals[0]
    count = counts[0] - 1
    store_lnk = Link(val)
    return two_list_helper(vals[1:], counts[1:], val, count, store_lnk, store_lnk)


